package p3_运算符.compareoperator;

public class CompareoperatorDemo1 {

    public static void main(String[] args) {
        // 1. == 判断左右两边是否相等
        int a = 10;
        int b = 10;
        int c = 20;
        System.out.println(a == b); // true
        System.out.println(a == c); // false

        // 2. != 判断两边是否不相等
    }

}
