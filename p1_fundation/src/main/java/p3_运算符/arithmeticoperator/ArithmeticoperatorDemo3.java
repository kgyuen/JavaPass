package p3_运算符.arithmeticoperator;

public class ArithmeticoperatorDemo3 {

    public static void main(String[] args) {
        byte b1 = 10;
        byte b2 = 20;
        // 强制转换 b1 + b2 计算之后的结果
        // (byte)(b1 + b2)
        byte result = (byte) (b1 +b2);
        System.out.println(result);
    }
}
